

var far = prompt("Input Fahrenheit", "" );

var f = (far-32)*5/9;


if (far) {alert(f + " Degree Celsius");}


var cel = prompt("Input Celsius", "" );

var c = (cel*9/5)+32;

if (cel) {alert(c +" Degree Fahrenheit");}




document.write(far + "   Degree Fahrenheit   =  " + f + "  Degree Celsius <br><br>");



document.write( cel + "   Degree Celsius  =  " + c + "  Degree Fahrenheit");




